/**
 * Theme Setup
 */
function mpe2025_setup() {
	// Add default posts and comments RSS feed links to head.
	add_theme_support( 'automatic-feed-links' );

	// Let WordPress manage the document title.
	add_theme_support( 'title-tag' );

	// Enable support for Post Thumbnails on posts and pages.
	add_theme_support( 'post-thumbnails' );

	// add cutome img size

	add_image_size("thumb", 390, 220, true);

	// Switch default core markup for search form, comment form, and comments to output valid HTML5.
	add_theme_support( 'html5', array(
		'search-form',
		'comment-form',
		'comment-list',
		'gallery',
		'caption',
		'style',
		'script',
	) );

	// Register Navigation Menus
	register_nav_menus( array(
		'primary'      => 'Primary Menu',
		'footer_links' => 'Footer Quick Links',
	) );
}
add_action( 'after_setup_theme', 'mpe2025_setup' );

/**
 * Enqueue Scripts and Styles
 */
function mpe2025_scripts() {
	// Google Fonts - Now using local fonts for better performance
	// wp_enqueue_style( 'mpe2025-google-fonts', 'https://fonts.googleapis.com/css2?family=Plus+Jakarta+Sans:ital,wght@0,200..800;1,200..800&display=swap', array(), null );
	
	// Material Icons - Removed for performance
	// wp_enqueue_style( 'mpe2025-material-icons', 'https://fonts.googleapis.com/css2?family=Material+Symbols+Outlined:wght,FILL@100..700,0..1&display=swap', array(), null );

	// Dequeue WordPress block library styles (now integrated into our main SCSS to reduce render-blocking)
	wp_dequeue_style( 'wp-block-library' );
	wp_dequeue_style( 'wp-block-library-theme' );

	// Main Stylesheet
	$style_ver = file_exists( get_stylesheet_directory() . '/style.css' ) ? filemtime( get_stylesheet_directory() . '/style.css' ) : '1.0.0';
	wp_enqueue_style( 'mpe2025-style', get_stylesheet_uri(), array(), $style_ver );

    // Main Script
    $script_ver = file_exists( get_template_directory() . '/assets/js/main.js' ) ? filemtime( get_template_directory() . '/assets/js/main.js' ) : '1.0.0';
    wp_enqueue_script( 'mpe2025-main-script', get_template_directory_uri() . '/assets/js/main.js', array(), $script_ver, true );

	// Threaded comments
	if ( is_singular() && comments_open() && get_option( 'thread_comments' ) ) {
		wp_enqueue_script( 'comment-reply' );
	}
}
add_action( 'wp_enqueue_scripts', 'mpe2025_scripts' );

/**
 * Defer non-critical scripts
 */
function mpe2025_defer_scripts( $tag, $handle ) {
    // Do not defer scripts in admin dashboard as it breaks the editor
    if ( is_admin() ) {
        return $tag;
    }

    // List of scripts to defer
    $defer_scripts = array( 
        'wprs-unslider-swipe', 
        'wprev-public', 
        'jquery-migrate',
        'contact-form-7',
        'wp-block-library'
    );

    if ( in_array( $handle, $defer_scripts ) ) {
        return str_replace( ' src', ' defer="defer" src', $tag );
    }

    return $tag;
}
add_filter( 'script_loader_tag', 'mpe2025_defer_scripts', 10, 2 );

