/**
 * Pass available languages to reservation form block editor
 */
function mpe2025_enqueue_block_editor_assets() {
	// Get available languages for the block editor
	$languages = array();
	if ( function_exists( 'pll_languages_list' ) ) {
		$pll_languages = pll_languages_list( array( 'fields' => '' ) );
		foreach ( $pll_languages as $lang ) {
			$languages[] = array(
				'label' => $lang->name,
				'value' => $lang->slug,
			);
		}
	}
	
	// Fallback if Polylang is not active
	if ( empty( $languages ) ) {
		$languages = array(
			array( 'label' => 'English', 'value' => 'en' ),
			array( 'label' => 'Français', 'value' => 'fr' ),
		);
	}
	
	// The block script handle is auto-generated by WordPress as: mpe2025-reservation-form-editor-script
	// But when using file:./index.js in block.json, WordPress generates it differently
	// We need to use wp_add_inline_script on one of the core block editor scripts that loads early
	wp_add_inline_script(
		'wp-blocks',
		'window.mpeReservationFormLanguages = ' . wp_json_encode( $languages ) . ';',
		'before'
	);
}
add_action( 'enqueue_block_editor_assets', 'mpe2025_enqueue_block_editor_assets' );

